// JavaScript Primitive Data Types

// 1. Number
let num = 42;
console.log("Number:", num, "| type:", typeof num);

// 2. String
let str = "Hello, World!";
console.log("String:", str, "| type:", typeof str);

// 3. Boolean
let bool = true;
console.log("Boolean:", bool, "| type:", typeof bool);

// 4. Undefined
let undef;
console.log("Undefined:", undef, "| type:", typeof undef);

// 5. Null
let empty = null;
console.log("Null:", empty, "| type:", typeof empty);

// 6. BigInt
let bigIntVal = 1234567890123456789012345678901234567890n;
console.log("BigInt:", bigIntVal, "| type:", typeof bigIntVal);

// 7. Symbol
let sym = Symbol("unique");
console.log("Symbol:", sym, "| type:", typeof sym);

// Summary
console.log("\nPrimitive data types in JavaScript: Number, String, Boolean, Undefined, Null, BigInt, Symbol.");